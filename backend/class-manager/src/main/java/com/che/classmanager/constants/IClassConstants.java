package com.che.classmanager.constants;

/**
 * The interface IClassConstants. It holds class related constants.
 */
public interface IClassConstants {

	/**
	 * The class name regex
	 */
	public static final String CLASSNAMEREGEX = "^[A-Z][a-zA-Z0-9]*$";

	/**
	 * The class ID regex
	 */
	public static final String CLASSIDREGEX = "^[\\d]+$";
}
